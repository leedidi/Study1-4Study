FROM JOBS
WHERE JOB_TITLE='Sales Manager';
--==>> SA_MAN
      
      
UPDATE EMPLOYEES
SET SALARY = (SELECT MAX_SALARY
              FROM JOBS
              WHERE JOB_TITLE='Sales Manager')
WHERE JOB_ID = (SELECT JOB_ID
                FROM JOBS
                WHERE JOB_TITLE='Sales Manager')
      AND TO_NUMBER(TO_CHAR(HIRE_DATE, 'YYYY')) < 2006; --@ 조건이니까 AND/OR 사용!
--==>> 3개 행 이(가) 업데이트되었습니다.

